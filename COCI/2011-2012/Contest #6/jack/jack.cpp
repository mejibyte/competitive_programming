int main(){
    int n, m;
    cin >> n >> m;
    vector<int> cards(n);
    for (int i = 0; i < n; ++i) cin >> cards[i];
    int best = -1;
    for (int i = 0; i < n; ++i)
        for (int j = i + 1; j < n; ++j)
            for (int k = j + 1; k < n; ++k) {
                int sum = cards[i] + cards[j] + cards[k];
                if (sum <= m and sum > best) best = sum;
            }
    cout << best << endl;
    return 0;
}