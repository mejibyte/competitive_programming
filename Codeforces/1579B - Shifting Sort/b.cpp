void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    vector<vector<int>> answer;
    for (int i = 0; i < n; ++i) {
        int next = min_element(a.begin() + i, a.end()) - (a.begin() + i);
        if (next > 0) {
            answer.push_back({i, n-1, next});
            rotate(a.begin() + i, a.begin() + i + next, a.end());
        }
    }
    cout << answer.size() << endl;
    for (auto &v : answer) {
        cout << v[0] + 1 << " " << v[1] + 1 << " " << v[2] << endl;
    }
}

